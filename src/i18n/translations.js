function mergeMissingTranslations(target, fallback) {
  Object.entries(fallback).forEach(([key, fallbackValue]) => {
    const targetValue = target[key];

    if (
      fallbackValue &&
      typeof fallbackValue === 'object' &&
      !Array.isArray(fallbackValue)
    ) {
      if (!targetValue || typeof targetValue !== 'object') {
        target[key] = {};
      }
      mergeMissingTranslations(target[key], fallbackValue);
      return;
    }

    if (targetValue === undefined) {
      target[key] = fallbackValue;
    }
  });
}

Object.keys(translations).forEach((language) => {
  if (language !== 'en') {
    mergeMissingTranslations(translations[language], translations.en);
  }
});
